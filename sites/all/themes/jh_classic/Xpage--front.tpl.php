	<h2>Who We Are</h2>
	<p>LA Loves You (LALY) is a nonprofit 501(c)(3) organization with the mission of engaging LA's next generation of philanthropists in charitable giving. LALY offers a new brand of civic organization. We cater to 20 to 40 somethings, but welcome everyone who's young at heart and ready to make a difference. By combining our talent and resources, we hope to strengthen our community. </p>

	<h2>What We Do</h2>
	<p>LALY combines social engagement with meaningful contribution. We host unique, high-end events to raise funds and awareness for deserving causes. Rather than focusing on a single cause, we support a wide variety of charities, nominated by the community and selected by our Board. </p>
	
